document.addEventListener('DOMContentLoaded', function () {
    document.querySelectorAll('.equipment-gallery__card').forEach(function (card) {
        var track = card.querySelector('.equipment-gallery__track');
        var slides = card.querySelectorAll('.equipment-gallery__slide');
        if (!track || slides.length <= 1) return;
        var prevBtn = card.querySelector('.equipment-gallery__nav-btn[data-dir="prev"]');
        var nextBtn = card.querySelector('.equipment-gallery__nav-btn[data-dir="next"]');
        var counter = card.querySelector('.equipment-gallery__counter');
        var total = slides.length;
        var current = 0;
        function update() {
            track.style.transform = 'translateX(-' + (current * 100) + '%)';
            if (counter) counter.textContent = (current + 1) + '/' + total;
        }
        function go(idx) {
            if (idx < 0) idx = total - 1;
            if (idx >= total) idx = 0;
            current = idx;
            update();
        }
        if (prevBtn) prevBtn.addEventListener('click', function () { go(current - 1); });
        if (nextBtn) nextBtn.addEventListener('click', function () { go(current + 1); });
        var touchStartX = 0;
        track.addEventListener('touchstart', function (e) {
            touchStartX = e.changedTouches[0].screenX;
        }, { passive: true });
        track.addEventListener('touchend', function (e) {
            var diff = e.changedTouches[0].screenX - touchStartX;
            if (diff < -40) go(current + 1);
            if (diff > 40) go(current - 1);
        }, { passive: true });
    });
});
</script>

